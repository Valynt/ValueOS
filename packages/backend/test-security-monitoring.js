#!/usr/bin/env node

/**
 * Minimal test script for security monitoring functionality
 * Tests the enhanced security logger and API endpoints
 */

import { SecurityMetricsCollector } from "./src/security/enhancedSecurityLogger.ts";

// Test the security logger
console.log("Testing Security Monitoring...");

const collector = SecurityMetricsCollector.getInstance();

// Simulate some security events
collector.recordEvent({
  type: "authentication",
  category: "auth",
  severity: "info",
  outcome: "success",
  reason: "User login successful",
  metadata: { userId: "test-user", ip: "127.0.0.1" },
});

collector.recordEvent({
  type: "authorization",
  category: "access",
  severity: "warning",
  outcome: "denied",
  reason: "Insufficient permissions",
  metadata: { userId: "test-user", resource: "admin-panel" },
});

collector.recordEvent({
  type: "input_validation",
  category: "validation",
  severity: "error",
  outcome: "blocked",
  reason: "XSS attempt detected",
  metadata: { userId: "test-user", input: "<script>alert('xss')</script>" },
});

collector.recordEvent({
  type: "ai_code_inspection",
  category: "code_generation",
  severity: "info",
  outcome: "approved",
  reason: "Code generated by Roo Code",
  metadata: { tool: "Roo Code", language: "javascript" },
});

collector.recordEvent({
  type: "ai_code_inspection",
  category: "code_generation",
  severity: "warning",
  outcome: "flagged",
  reason: "Potential issue in code generated by Codex",
  metadata: { tool: "Codex", language: "python" },
});

// Get metrics
const metrics = collector.getMetrics();
console.log("Security Metrics:", JSON.stringify(metrics, null, 2));

// Get recent events
const events = collector.getRecentEvents(10);
console.log("Recent Events:", JSON.stringify(events, null, 2));

console.log("✅ Security monitoring test completed successfully!");
