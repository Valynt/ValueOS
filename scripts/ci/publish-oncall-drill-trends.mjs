#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';

const drillsPath = '.github/metrics/oncall-drills.json';
const trendPath = '.github/metrics/oncall-drill-trends.json';
const scorecardPath = 'docs/operations/on-call-drill-scorecard.md';

if (!fs.existsSync(drillsPath)) {
  throw new Error(`Missing drill dataset at ${drillsPath}`);
}

const drills = JSON.parse(fs.readFileSync(drillsPath, 'utf8'));
if (!Array.isArray(drills)) {
  throw new Error('oncall drill dataset must be an array');
}

const normalized = drills
  .map((d) => ({
    date: d.date,
    scenario: d.scenario ?? 'unspecified',
    owner: d.owner ?? 'team-sre',
    sloTargetMinutes: Number(d.sloTargetMinutes),
    mttrMinutes: Number(d.mttrMinutes),
  }))
  .filter((d) => d.date && Number.isFinite(d.sloTargetMinutes) && Number.isFinite(d.mttrMinutes))
  .sort((a, b) => a.date.localeCompare(b.date));

const byMonth = new Map();
for (const entry of normalized) {
  const month = entry.date.slice(0, 7);
  const bucket = byMonth.get(month) ?? [];
  bucket.push(entry);
  byMonth.set(month, bucket);
}

const monthly = [...byMonth.entries()].map(([month, entries]) => {
  const avgMttr = entries.reduce((sum, e) => sum + e.mttrMinutes, 0) / entries.length;
  const avgTarget = entries.reduce((sum, e) => sum + e.sloTargetMinutes, 0) / entries.length;
  const metCount = entries.filter((e) => e.mttrMinutes <= e.sloTargetMinutes).length;
  return {
    month,
    drillCount: entries.length,
    avgMttrMinutes: Number(avgMttr.toFixed(2)),
    avgSloTargetMinutes: Number(avgTarget.toFixed(2)),
    sloAttainmentPercent: Number(((metCount / entries.length) * 100).toFixed(2)),
  };
});

const latest = monthly[monthly.length - 1];
const previous = monthly[monthly.length - 2];
const mttrDelta = previous ? Number((latest.avgMttrMinutes - previous.avgMttrMinutes).toFixed(2)) : 0;
const trend = mttrDelta < 0 ? 'improving' : mttrDelta > 0 ? 'regressing' : 'flat';

const trendPayload = {
  generatedAt: new Date().toISOString(),
  totalDrills: normalized.length,
  latestMonth: latest?.month ?? null,
  trend,
  mttrDelta,
  monthly,
};

fs.mkdirSync(path.dirname(trendPath), { recursive: true });
fs.writeFileSync(trendPath, JSON.stringify(trendPayload, null, 2) + '\n');

const rows = normalized
  .slice()
  .reverse()
  .map((d) => `| ${d.date} | ${d.scenario} | ${d.owner} | ${d.mttrMinutes}m | ${d.sloTargetMinutes}m | ${d.mttrMinutes <= d.sloTargetMinutes ? '✅' : '❌'} |`)
  .join('\n');

const monthlyRows = monthly
  .map((m) => `| ${m.month} | ${m.drillCount} | ${m.avgMttrMinutes}m | ${m.avgSloTargetMinutes}m | ${m.sloAttainmentPercent}% |`)
  .join('\n');

const reviewDate = new Date(Date.now() + 1000 * 60 * 60 * 24 * 90).toISOString().slice(0, 10);

const scorecard = `---
title: On-Call Drill Scorecard
owner: team-sre
escalation_path: "pagerduty://valueos-primary -> slack:#incident-response -> email:platform-leadership@valueos.com"
review_date: ${reviewDate}
status: active
---

# On-Call Drill Scorecard

Auto-generated by \`scripts/ci/publish-oncall-drill-trends.mjs\`.

## MTTR vs SLO Trend

- Latest monthly trend: **${trend}**
- Latest month: **${latest?.month ?? 'n/a'}**
- MTTR delta vs previous month: **${mttrDelta} minutes**

| Month | Drill Count | Avg MTTR | Avg SLO Target | SLO Attainment |
| --- | ---: | ---: | ---: | ---: |
${monthlyRows}

## Drill Log

| Date | Scenario | Owner | MTTR | SLO Target | Met Target |
| --- | --- | --- | ---: | ---: | --- |
${rows}
`;

fs.mkdirSync(path.dirname(scorecardPath), { recursive: true });
fs.writeFileSync(scorecardPath, scorecard);
console.log(`Published scorecard -> ${scorecardPath}`);
console.log(`Published trend payload -> ${trendPath}`);
