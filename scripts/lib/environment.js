import fs from 'fs';
import path from 'path';
import crypto from 'crypto';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

/**
 * Generate cryptographically secure random string
 */
function generateSecret(bytes = 32) {
  return crypto.randomBytes(bytes).toString('hex');
}

/**
 * Generate secure secrets
 */
export function generateSecrets() {
  return {
    JWT_SECRET: generateSecret(32),
    SUPABASE_JWT_SECRET: generateSecret(32),
    SESSION_SECRET: generateSecret(32),
    ENCRYPTION_KEY: generateSecret(32),
    DATABASE_PASSWORD: generateSecret(16),
    REDIS_PASSWORD: generateSecret(16)
  };
}

/**
 * Read .env.example template
 */
function readEnvTemplate() {
  const projectRoot = path.resolve(__dirname, '../..');
  const templatePath = path.join(projectRoot, '.env.example');
  
  if (!fs.existsSync(templatePath)) {
    console.log('⚠️  .env.example not found, creating minimal template');
    return `# ValueOS Environment Configuration
# Generated by setup script

NODE_ENV=development

# Database
DATABASE_URL=postgresql://postgres:postgres@localhost:54322/postgres
VITE_SUPABASE_URL=http://localhost:54321
VITE_SUPABASE_ANON_KEY=

# Secrets (auto-generated)
JWT_SECRET=
SUPABASE_JWT_SECRET=
SESSION_SECRET=

# Redis
REDIS_URL=redis://localhost:6379

# Frontend
VITE_APP_URL=http://localhost:5173

# Backend
BACKEND_URL=http://localhost:3000
PORT=3000
`;
  }

  return fs.readFileSync(templatePath, 'utf8');
}

/**
 * Parse environment template
 */
function parseEnvTemplate(template) {
  const lines = template.split('\n');
  const vars = {};
  
  lines.forEach(line => {
    const trimmed = line.trim();
    if (trimmed && !trimmed.startsWith('#')) {
      const [key, ...valueParts] = trimmed.split('=');
      if (key) {
        vars[key.trim()] = valueParts.join('=').trim();
      }
    }
  });
  
  return vars;
}

