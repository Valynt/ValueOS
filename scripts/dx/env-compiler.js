/**
 * Validate existing .env.local matches the expected mode
 */
function validateEnvLocal(expectedMode) {
  const envPath = path.join(projectRoot, ".env.local");

  if (!fs.existsSync(envPath)) {
    return { valid: false, error: ".env.local does not exist" };
  }

  const content = fs.readFileSync(envPath, "utf8");
  const errors = [];
  const warnings = [];

  // Check DX_MODE
  const modeMatch = content.match(/^DX_MODE=(.*)$/m);
  const currentMode = modeMatch ? modeMatch[1].trim() : null;

  if (!currentMode) {
    errors.push("DX_MODE not set in .env.local");
  } else if (currentMode !== expectedMode) {
    errors.push(
      `DX_MODE mismatch: .env.local has "${currentMode}" but expected "${expectedMode}"`
    );
  }

  // Check for deprecated SUPABASE_SERVICE_KEY (should be SUPABASE_SERVICE_ROLE_KEY)
  if (content.includes("SUPABASE_SERVICE_KEY=")) {
    warnings.push(
      'Deprecated: SUPABASE_SERVICE_KEY should be SUPABASE_SERVICE_ROLE_KEY'
    );
  }

  // Check URL consistency for mode
  const ports = loadPorts();
  const urls = getUrlConfig(expectedMode, ports);

  // Check VITE_API_BASE_URL
  const apiUrlMatch = content.match(/^VITE_API_BASE_URL=(.*)$/m);
  if (apiUrlMatch) {
    const currentApiUrl = apiUrlMatch[1].trim();
    if (expectedMode === "local") {
      // Local mode should use localhost URLs
      if (
        currentApiUrl.includes("backend:") ||
        currentApiUrl.includes("frontend:")
      ) {
        errors.push(
          `VITE_API_BASE_URL uses Docker DNS (${currentApiUrl}) but mode is "local". Browser cannot resolve Docker hostnames.`
        );
      }
    }
  }

  // Check Supabase URL
  const supabaseUrlMatch = content.match(/^VITE_SUPABASE_URL=(.*)$/m);
  if (supabaseUrlMatch) {
    const currentSupabaseUrl = supabaseUrlMatch[1].trim();
    if (
      !currentSupabaseUrl.includes("localhost") &&
      !currentSupabaseUrl.includes("127.0.0.1")
    ) {
      warnings.push(
        `VITE_SUPABASE_URL points to remote instance (${currentSupabaseUrl}). Local Supabase will not be used.`
      );
    }
  }

  // Check for placeholder values
  const placeholderPatterns = [
    /VITE_SUPABASE_ANON_KEY=your-/,
    /VITE_SUPABASE_ANON_KEY=placeholder/,
    /SUPABASE_SERVICE_ROLE_KEY=your-/,
    /SUPABASE_SERVICE_ROLE_KEY=placeholder/,
  ];

  for (const pattern of placeholderPatterns) {
    if (pattern.test(content)) {
      errors.push(`Placeholder value found: ${pattern.source}`);
    }
  }

  return {
    valid: errors.length === 0,
    errors,
    warnings,
    currentMode,
  };
}

/**
 * Write environment files
 */
function writeEnvFiles(mode, options = {}) {
  const ports = loadPorts();
  const dryRun = options.dryRun || false;
  const force = options.force || false;

  const envLocalPath = path.join(projectRoot, ".env.local");
  const envPortsPath = path.join(projectRoot, ".env.ports");

  // Check if .env.local exists and has different mode
  if (fs.existsSync(envLocalPath) && !force) {
    const validation = validateEnvLocal(mode);
    if (validation.currentMode && validation.currentMode !== mode) {
      console.error(`
❌ Mode mismatch detected!

Current .env.local is configured for mode: ${validation.currentMode}
You requested mode: ${mode}

This can cause issues where the browser tries to connect to Docker hostnames
that only work inside the container network.

To fix:
  1. Run: npm run dx:env --mode ${mode} --force
  2. Or manually update .env.local

`);
      process.exit(1);
    }
  }

  const envLocalContent = generateEnvLocal(mode, ports);
  const envPortsContent = generateEnvPorts(ports);

  if (dryRun) {
    console.log("=== .env.local (dry run) ===");
    console.log(envLocalContent);
    console.log("\n=== .env.ports (dry run) ===");
    console.log(envPortsContent);
    return;
  }

  // Write .env.local
  fs.writeFileSync(envLocalPath, envLocalContent, "utf8");
  console.log(`✓ Wrote ${envLocalPath} for mode: ${mode}`);

  // Write .env.ports
  fs.writeFileSync(envPortsPath, envPortsContent, "utf8");
  console.log(`✓ Wrote ${envPortsPath}`);

  // Also update deploy/envs/.env.ports for Docker Compose
  const deployEnvPortsPath = path.join(
    projectRoot,
    "deploy/envs/.env.ports"
  );
  if (fs.existsSync(path.dirname(deployEnvPortsPath))) {
    fs.writeFileSync(deployEnvPortsPath, envPortsContent, "utf8");
    console.log(`✓ Wrote ${deployEnvPortsPath}`);
  }

  console.log(`
Environment configured for mode: ${mode}

URLs configured:
  Frontend: http://localhost:${ports.frontend.port}
  Backend:  ${mode === "local" ? `http://localhost:${ports.backend.port}` : "/api (via Caddy)"}
  Supabase: http://localhost:${ports.supabase.apiPort}

Next steps:
  npm run dx --mode ${mode}
`);
}

/**
 * Main CLI
 */
function main() {
  const args = process.argv.slice(2);

  // Parse arguments
  const validateOnly = args.includes("--validate");
  const dryRun = args.includes("--dry-run");
  const force = args.includes("--force");

  let mode;
  try {
    mode = resolveMode(args);
  } catch (error) {
    console.error(error.message);
    process.exit(1);
  }

  console.log(`DX Environment Compiler`);
  console.log(`Mode: ${mode}`);
  console.log("");

  if (validateOnly) {
    const validation = validateEnvLocal(mode);

    if (validation.warnings.length > 0) {
      console.log("⚠️  Warnings:");
      validation.warnings.forEach((w) => console.log(`   - ${w}`));
      console.log("");
    }

    if (validation.valid) {
      console.log(`✓ .env.local is valid for mode: ${mode}`);
      process.exit(0);
    } else {
      console.log("❌ Validation failed:");
      validation.errors.forEach((e) => console.log(`   - ${e}`));
      console.log("");
      console.log(`Fix: npm run dx:env --mode ${mode} --force`);
      process.exit(1);
    }
  }

  writeEnvFiles(mode, { dryRun, force });
}

