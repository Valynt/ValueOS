  /**
   * Distribute artifact to recipients
   */
  async distributeArtifact(
    artifactId: string,
    versionId: string,
    options: {
      email?: string[];
      webhooks?: string[];
      retentionDays?: number;
    }
  ): Promise<DistributionRecord[]> {
    const records: DistributionRecord[] = [];

    // Distribute via email
    if (options.email && options.email.length > 0) {
      for (const email of options.email) {
        const record = await this.distributeViaEmail(artifactId, versionId, email);
        records.push(record);
      }
    }

    // Distribute via webhooks
    if (options.webhooks && options.webhooks.length > 0) {
      for (const webhook of options.webhooks) {
        const record = await this.distributeViaWebhook(artifactId, versionId, webhook);
        records.push(record);
      }
    }

    return records;
  }

