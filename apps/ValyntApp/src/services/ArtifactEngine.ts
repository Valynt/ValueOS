  /**
   * Generate an artifact based on request
   */
  async generateArtifact(
    request: ArtifactGenerationRequest,
    onProgress?: (progress: ArtifactProgress) => void
  ): Promise<ArtifactGenerationResult> {
    try {
      onProgress?.({
        status: "preparing",
        progress: 10,
        message: "Preparing artifact generation...",
      });

      // Get value case data
      const valueCase = await this.getValueCaseData(request.valueCaseId);
      if (!valueCase) {
        throw new Error(`Value case ${request.valueCaseId} not found`);
      }

      // Get branding profile
      let brandingProfile: BrandingProfile | undefined;
      if (request.brandingProfileId) {
        brandingProfile = await this.getBrandingProfile(request.brandingProfileId);
      }

      // Get template
      const template = this.templates.get(request.artifactType);
      if (!template) {
        throw new Error(`No template found for artifact type: ${request.artifactType}`);
      }

      onProgress?.({
        status: "generating",
        progress: 40,
        message: "Generating artifact content...",
      });

      // Generate artifact data
      const artifactData = await this.generateArtifactData(
        template,
        valueCase,
        brandingProfile
      );

      onProgress?.({
        status: "versioning",
        progress: 60,
        message: "Creating version...",
      });

      // Create artifact
      const artifact = await this.createArtifact({
        organizationId: valueCase.organizationId,
        tenantId: valueCase.tenantId,
        type: request.artifactType,
        format: request.format,
        name: `${template.name} - ${valueCase.companyName}`,
        description: template.metadata.description,
        valueCaseId: request.valueCaseId,
        brandingProfileId: request.brandingProfileId,
        distributionOptions: request.distributionOptions,
        status: "generating",
        createdBy: valueCase.createdBy,
      });

      // Create version
      const version = await this.createArtifactVersion(artifact.id, request.format, artifactData, {
        generatedBy: valueCase.createdBy,
        dataHash: this.generateDataHash(artifactData),
        sourceDataVersion: valueCase.version,
        assumptions: valueCase.assumptions,
      });

      onProgress?.({
        status: "storing",
        progress: 80,
        message: "Storing artifact...",
        artifactId: artifact.id,
        versionId: version.id,
      });

      // Store artifact
      const storageMetadata = await this.storeArtifact(
        artifact.id,
        version.id,
        request.format,
        artifactData
      );

      // Update artifact status
      await this.updateArtifact(artifact.id, { status: "ready" });

      onProgress?.({
        status: "distributing",
        progress: 90,
        message: "Distributing artifact...",
      });

      // Distribute if requested
      if (request.distributionOptions) {
        await this.distributeArtifact(artifact.id, version.id, request.distributionOptions);
      }

      onProgress?.({
        status: "complete",
        progress: 100,
        message: "Artifact generation complete!",
        artifactId: artifact.id,
        versionId: version.id,
      });

      // Emit event
      this.emit("artifact_generated", {
        artifactId: artifact.id,
        versionId: version.id,
        format: request.format,
      });

      return {
        success: true,
        artifactId: artifact.id,
        versionId: version.id,
        format: request.format,
        data: artifactData,
        metadata: {
          generatedAt: new Date().toISOString(),
          generationTimeMs: Date.now() - Date.now(), // Placeholder
          dataHash: version.metadata.dataHash,
        },
      };
    } catch (error) {
      onProgress?.({
        status: "error",
        progress: 0,
        message: error instanceof Error ? error.message : "Generation failed",
      });

      return {
        success: false,
        errors: [error instanceof Error ? error.message : "Unknown error"],
      };
    }
  }

  // ============================================================================
  // Version Management
  // ============================================================================

  /**
   * Create a new version of an artifact
   */
  async createArtifactVersion(
    artifactId: string,
    format: ArtifactFormat,
    data: unknown,
    metadata: {
      generatedBy: string;
      dataHash: string;
      sourceDataVersion?: string;
      assumptions?: unknown[];
    }
  ): Promise<ArtifactVersion> {
    const artifact = await this.getArtifact(artifactId);
    if (!artifact) {
      throw new Error(`Artifact ${artifactId} not found`);
    }

    // Increment version (simple semantic versioning)
    const currentVersion = artifact.currentVersion;
    const [major, minor, patch] = currentVersion.split(".").map(Number);
    const newVersion = `${major}.${minor}.${patch + 1}`;

    const version: ArtifactVersion = {
      id: crypto.randomUUID(),
      artifactId,
      version: newVersion,
      format,
      data,
      metadata: {
        generatedAt: new Date().toISOString(),
        ...metadata,
      },
      createdAt: new Date().toISOString(),
    };

    const { data: inserted, error } = await this.supabase
      .from("artifact_versions")
      .insert(version)
      .select()
      .single();

    if (error) {
      throw new Error(`Failed to create version: ${error.message}`);
    }

    // Update artifact with new version
    await this.updateArtifact(artifactId, {
      currentVersion: newVersion,
      versionHistory: [...artifact.versionHistory, newVersion],
    });

    // Emit event
    this.emit("artifact_versioned", { artifactId, versionId: version.id, version: newVersion });

    return inserted;
  }

