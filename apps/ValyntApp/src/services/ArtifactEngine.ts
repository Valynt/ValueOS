  /**
   * Create a new artifact
   */
  async createArtifact(input: CreateArtifact): Promise<Artifact> {
    const artifact: Artifact = {
      id: crypto.randomUUID(),
      ...input,
      currentVersion: "1.0.0",
      versionHistory: ["1.0.0"],
      status: input.status || "draft",
      createdAt: new Date().toISOString(),
      updatedAt: new Date().toISOString(),
    };

    const { data, error } = await this.supabase
      .from("workflow_artifacts")
      .insert(artifact)
      .select()
      .single();

    if (error) {
      throw new Error(`Failed to create artifact: ${error.message}`);
    }

    // Emit event
    this.emit("artifact_created", { artifactId: artifact.id });

    return data;
  }

  /**
   * Update an artifact
   */
  async updateArtifact(artifactId: string, updates: UpdateArtifact): Promise<Artifact> {
    const updatesWithTimestamp = {
      ...updates,
      updatedAt: new Date().toISOString(),
    };

    const { data, error } = await this.supabase
      .from("workflow_artifacts")
      .update(updatesWithTimestamp)
      .eq("id", artifactId)
      .select()
      .single();

    if (error) {
      throw new Error(`Failed to update artifact: ${error.message}`);
    }

    // Emit event
    this.emit("artifact_updated", { artifactId });

    return data;
  }

  /**
   * Get an artifact by ID
   */
  async getArtifact(artifactId: string): Promise<Artifact | null> {
    const { data, error } = await this.supabase
      .from("workflow_artifacts")
      .select()
      .eq("id", artifactId)
      .single();

    if (error) {
      if (error.code === "PGRST116") {
        return null; // Not found
      }
      throw new Error(`Failed to get artifact: ${error.message}`);
    }

    return data;
  }

  /**
   * List artifacts with filters
   */
  async listArtifacts(filters: {
    organizationId?: string;
    tenantId?: string;
    type?: ArtifactType;
    status?: Artifact["status"];
    createdBy?: string;
    limit?: number;
    offset?: number;
  }): Promise<Artifact[]> {
    let query = this.supabase.from("workflow_artifacts").select();

    if (filters.organizationId) {
      query = query.eq("organizationId", filters.organizationId);
    }
    if (filters.tenantId) {
      query = query.eq("tenantId", filters.tenantId);
    }
    if (filters.type) {
      query = query.eq("type", filters.type);
    }
    if (filters.status) {
      query = query.eq("status", filters.status);
    }
    if (filters.createdBy) {
      query = query.eq("createdBy", filters.createdBy);
    }

    if (filters.limit) {
      query = query.limit(filters.limit);
    }
    if (filters.offset) {
      query = query.range(filters.offset, filters.offset + (filters.limit || 10) - 1);
    }

    const { data, error } = await query;

    if (error) {
      throw new Error(`Failed to list artifacts: ${error.message}`);
    }

    return data || [];
  }

