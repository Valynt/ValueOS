import { z } from "zod";

export const ValueCommitmentSchema = z.object({
  id: z.string().optional(),
  tenant_id: z.string(),
  session_id: z.string(),
  user_id: z.string(),
  organization_id: z.string().nullable().optional(),
  title: z.string(),
  description: z.string(),
  commitment_type: z.enum(['financial', 'strategic', 'operational']),
  priority: z.enum(['low', 'medium', 'high', 'critical']),
  financial_impact: z.object({
    revenue_uplift: z.number().optional(),
    cost_reduction: z.number().optional(),
    cost_avoidance: z.number().optional(),
    efficiency_gain: z.number().optional(),
  }),
  currency: z.string(),
  timeframe_months: z.number(),
  status: z.enum(['draft', 'pending_approval', 'in_progress', 'at_risk', 'completed', 'cancelled']),
  progress_percentage: z.number(),
  confidence_level: z.number(),
  committed_at: z.string(),
  target_completion_date: z.string(),
  actual_completion_date: z.string().nullable().optional(),
  ground_truth_references: z.object({
    benchmark_ids: z.array(z.string()),
    persona: z.string(),
    industry: z.string(),
    confidence_sources: z.array(z.string()),
  }),
  tags: z.array(z.string()),
  metadata: z.record(z.any()),
  created_at: z.string().optional(),
  updated_at: z.string().optional(),
});

export const CommitmentStakeholderSchema = z.object({
  id: z.string().optional(),
  commitment_id: z.string(),
  tenant_id: z.string(),
  user_id: z.string(),
  role: z.enum(['owner', 'contributor', 'approver', 'observer']),
  responsibility: z.string(),
  accountability_percentage: z.number(),
  is_active: z.boolean().optional(),
  notification_preferences: z.object({
    email: z.boolean(),
    in_app: z.boolean(),
    frequency: z.enum(['real_time', 'daily', 'weekly']),
  }).optional(),
  joined_at: z.string(),
  last_active_at: z.string(),
  created_at: z.string().optional(),
  updated_at: z.string().optional(),
});

export const CommitmentMilestoneSchema = z.object({
  id: z.string().optional(),
  commitment_id: z.string(),
  tenant_id: z.string(),
  title: z.string(),
  description: z.string(),
  due_date: z.string(),
  actual_date: z.string().nullable().optional(),
  status: z.enum(['pending', 'in_progress', 'completed', 'delayed', 'skipped']).optional(),
  progress_percentage: z.number().optional(),
  dependencies: z.array(z.string()).optional(),
  metadata: z.record(z.any()).optional(),
  created_at: z.string().optional(),
  updated_at: z.string().optional(),
});

export const CommitmentMetricSchema = z.object({
  id: z.string().optional(),
  commitment_id: z.string(),
  tenant_id: z.string(),
  metric_name: z.string(),
  metric_type: z.enum(['financial', 'operational', 'satisfaction', 'adoption']),
  unit: z.string(),
  target_value: z.number(),
  current_value: z.number(),
  baseline_value: z.number(),
  measurement_frequency: z.enum(['daily', 'weekly', 'monthly', 'quarterly']),
  data_source: z.string(),
  is_active: z.boolean().optional(),
  last_measured_at: z.string().nullable().optional(),
  created_at: z.string().optional(),
  updated_at: z.string().optional(),
});

export const CommitmentRiskSchema = z.object({
  id: z.string().optional(),
  commitment_id: z.string(),
  tenant_id: z.string(),
  risk_title: z.string(),
  risk_description: z.string(),
  risk_type: z.enum(['financial', 'technical', 'market', 'operational']),
  probability: z.enum(['low', 'medium', 'high']),
  impact: z.enum(['low', 'medium', 'high']),
  risk_score: z.number(),
  mitigation_plan: z.string(),
  status: z.enum(['identified', 'mitigating', 'mitigated', 'accepted', 'occurred']).optional(),
  owner_id: z.string().nullable().optional(),
  identified_at: z.string(),
  mitigated_at: z.string().nullable().optional(),
  created_at: z.string().optional(),
  updated_at: z.string().optional(),
});

export const CommitmentAuditSchema = z.object({
  id: z.string().optional(),
  commitment_id: z.string(),
  tenant_id: z.string(),
  user_id: z.string(),
  action: z.enum(['created', 'updated', 'deleted', 'status_changed', 'stakeholder_added', 'stakeholder_removed', 'milestone_completed', 'metric_updated', 'risk_assessed']),
  previous_values: z.record(z.any()),
  new_values: z.record(z.any()),
  change_reason: z.string(),
  created_at: z.string().optional(),
});
