import { z } from "zod";

export const ValueCommitmentSchema = z.object({
  id: z.string().optional(),
  tenant_id: z.string(),
  user_id: z.string(),
  session_id: z.string(),
  organization_id: z.string().nullable().optional(),
  title: z.string(),
  description: z.string(),
  commitment_type: z.string(),
  priority: z.string(),
  financial_impact: z.object({
    revenue_uplift: z.number().optional(),
    cost_reduction: z.number().optional(),
  }),
  currency: z.string(),
  timeframe_months: z.number(),
  status: z.enum(['draft', 'active', 'completed', 'cancelled', 'at_risk', 'in_progress']).default('draft'),
  progress_percentage: z.number().default(0),
  confidence_level: z.number().default(0),
  committed_at: z.string().datetime().optional(),
  target_completion_date: z.string().datetime(),
  actual_completion_date: z.string().datetime().nullable().optional(),
  ground_truth_references: z.record(z.any()).default({}),
  tags: z.array(z.string()).default([]),
  metadata: z.record(z.any()).default({}),
});

export const CommitmentStakeholderSchema = z.object({
  id: z.string().optional(),
  commitment_id: z.string(),
  tenant_id: z.string(),
  user_id: z.string(),
  role: z.string(),
  responsibility: z.string(),
  accountability_percentage: z.number(),
  is_active: z.boolean().default(true),
  notification_preferences: z.record(z.any()).default({}),
  joined_at: z.string().datetime().optional(),
  last_active_at: z.string().datetime().optional(),
});

export const CommitmentMilestoneSchema = z.object({
  id: z.string().optional(),
  commitment_id: z.string(),
  tenant_id: z.string(),
  title: z.string(),
  description: z.string(),
  due_date: z.string().datetime(),
  actual_date: z.string().datetime().nullable().optional(),
  status: z.enum(['pending', 'in_progress', 'completed', 'delayed', 'skipped']).default('pending'),
  progress_percentage: z.number().default(0),
  weight: z.number().default(1),
  owner_id: z.string(),
  dependencies: z.array(z.string()).default([]),
  deliverables: z.array(z.record(z.any())).default([]),
});

export const CommitmentMetricSchema = z.object({
  id: z.string().optional(),
  commitment_id: z.string(),
  tenant_id: z.string(),
  metric_name: z.string(),
  metric_type: z.string(),
  baseline_value: z.number(),
  target_value: z.number(),
  current_value: z.number(),
  unit: z.string(),
  data_source: z.string(),
  collection_frequency: z.string(),
  is_active: z.boolean().default(true),
  last_measured_at: z.string().datetime().optional(),
  next_measurement_due: z.string().datetime(),
});

export const CommitmentRiskSchema = z.object({
  id: z.string().optional(),
  commitment_id: z.string(),
  tenant_id: z.string(),
  risk_title: z.string(),
  risk_description: z.string(),
  risk_category: z.string(),
  risk_probability: z.enum(['low', 'medium', 'high']),
  risk_impact: z.enum(['low', 'medium', 'high']),
  risk_score: z.number(),
  mitigation_strategy: z.string(),
  owner_id: z.string(),
  status: z.enum(['active', 'mitigated', 'accepted', 'occurred']).default('active'),
  identified_at: z.string().datetime().optional(),
  mitigated_at: z.string().datetime().nullable().optional(),
});

export const CommitmentAuditSchema = z.object({
  id: z.string().optional(),
  commitment_id: z.string(),
  tenant_id: z.string(),
  user_id: z.string(),
  action: z.enum(['created', 'updated', 'deleted', 'status_changed', 'stakeholder_added', 'milestone_completed', 'metric_updated', 'risk_assessed']),
  previous_values: z.record(z.any()),
  new_values: z.record(z.any()),
  change_reason: z.string(),
  metadata: z.record(z.any()).optional(),
  performed_at: z.string().datetime().optional(),
});
